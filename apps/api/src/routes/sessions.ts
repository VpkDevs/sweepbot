/**
 * Recalculates session aggregate stats entirely in SQL.
 * Single UPDATE with correlated subqueries — replaces the original O(n)
 * application-layer fetch-all-transactions pattern.
 *
 * Executed after every batch of transactions and at session close.
 */
async function recalculateSessionStats(sessionId: string): Promise<{
  totalWagered: number
  totalWon: number
  netResult: number
  rtp: number | null
  spinCount: number
}> {
  const result = await db.execute<{
    total_wagered: string
    total_won: string
    net_result: string
    rtp: string | null
    spin_count: string
  }>(sql`
    UPDATE sessions
    SET
      total_wagered    = COALESCE(agg.tw,  0),
      total_won        = COALESCE(agg.tw2, 0),
      net_result       = COALESCE(agg.nr,  0),
      rtp              = agg.rtp,
      spin_count       = COALESCE(agg.sc,  0),
      last_activity_at = NOW()
    FROM (
      SELECT
        -- Monetary aggregations use NUMERIC type to avoid floating-point rounding drift.
        -- All arithmetic is performed in SQL, NOT in JavaScript, preventing cumulative errors.
        SUM(bet_amount::numeric)                                           AS tw,
        SUM(win_amount::numeric)                                           AS tw2,
        SUM(win_amount::numeric) - SUM(bet_amount::numeric)               AS nr,
        CASE
          WHEN SUM(bet_amount::numeric) = 0 THEN NULL
          ELSE ROUND(SUM(win_amount::numeric) / SUM(bet_amount::numeric) * 100, 2)
        END                                                                AS rtp,
        COUNT(*)                                                           AS sc
      FROM transactions
      WHERE session_id = ${sessionId}
    ) AS agg
    WHERE sessions.id = ${sessionId}
    RETURNING
      total_wagered,
      total_won,
      net_result,
      rtp,
      spin_count
  `)

  const row = (result as unknown as { rows: Array<Record<string, string | null>> }).rows[0]
  if (!row) return { totalWagered: 0, totalWon: 0, netResult: 0, rtp: null, spinCount: 0 }

  return {
    totalWagered: Number(row['total_wagered'] ?? 0),
    totalWon: Number(row['total_won'] ?? 0),
    netResult: Number(row['net_result'] ?? 0),
    rtp: row['rtp'] != null ? Number(row['rtp']) : null,
    spinCount: Number(row['spin_count'] ?? 0),
  }
}

/**
 * Enriches a session row with game metadata (name, theoretical RTP, volatility).
 * Joins games table via externalGameId + platformId — same compound key used in analytics.
 */
async function enrichWithGameMeta(
  gameId: string | null | undefined,
  platformId: string
): Promise<{
  game_name: string | null
  theoretical_rtp: number | null
  volatility_class: string | null
  game_provider: string | null
} | null> {
  if (!gameId) return null

  const [game] = await db
    .select({
      name: games.name,
      theoreticalRtp: games.theoreticalRtp,
      volatilityClass: games.volatilityClass,
      provider: games.provider,
    })
    .from(games)
    .where(
      and(
        eq(games.externalGameId, gameId),
        eq(games.platformId, platformId),
        eq(games.isActive, true)
      )
    )
    .limit(1)

  if (!game) return null

  return {
    game_name: game.name,
    theoretical_rtp: game.theoreticalRtp != null ? Number(game.theoreticalRtp) : null,
    volatility_class: game.volatilityClass ?? null,
    game_provider: game.provider ?? null,
  }
}

/**
 * Validates state machine transition. Throws structured error on invalid move.
 */
function assertValidTransition(currentStatus: string, targetStatus: string): void {
  const allowed = VALID_TRANSITIONS[currentStatus] ?? []
  if (!allowed.includes(targetStatus)) {
    throw {
      statusCode: 409,
      code: 'INVALID_STATUS_TRANSITION',
      message: `Cannot transition session from '${currentStatus}' to '${targetStatus}'. Allowed: [${allowed.join(', ') || 'none — status is terminal'}]`,
    }
  }
}

/**
 * Sends a structured error reply. Centralises error shape so every route
 * returns the same { success: false, error: { code, message, status } } envelope.
 *
 * The status field is included for client-side error handling consistency.
 */
function sendError(
  reply: { code: (n: number) => { send: (body: unknown) => unknown } },
  statusCode: number,
  code: string,
  message: string
) {
  return reply.code(statusCode).send({
    success: false,
    error: {
      code,
      message,
      status: statusCode,
    },
  })
}

