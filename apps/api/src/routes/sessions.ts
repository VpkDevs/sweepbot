/**
 * Sessions API — SweepBot
 *
 * Handles gameplay session lifecycle from the browser extension:
 *   - Idempotent session creation (client_session_id dedup key)
 *   - State machine enforcement: active ↔ paused → completed (terminal)
 *   - SQL-level aggregation — no O(n) application-layer scans
 *   - Idempotent batch transaction upserts (client_tx_id dedup)
 *   - Analytics cache eviction on every write
 *   - Game metadata enrichment pipeline
 *   - Full list endpoint with pagination, filters, and sort
 *   - Automatic stale-session detection and completion
 *   - Heartbeat endpoint to reset inactivity timer
 */

import type { FastifyPluginAsync } from 'fastify'
import { z } from 'zod'
import { db } from '../db/client.js'
import { sessions, transactions, platforms, games } from '../db/schema/comprehensive.js'
import { eq, and, desc, asc, sql, gte, lte, inArray, isNull, or } from 'drizzle-orm'
import { requireAuth } from '../middleware/auth.js'
import { evictUserAnalyticsCache } from './analytics.js'

// ============================================================================
// Constants & Configuration
// ============================================================================

/** Maximum transactions accepted in a single batch payload. */
const BATCH_TX_MAX = 500

/** Sessions inactive longer than this are auto-completed on heartbeat miss. */
const STALE_THRESHOLD_HOURS = 4

/** Valid state machine transitions. 'completed' is terminal — no exit. */
const VALID_TRANSITIONS: Record<string, readonly string[]> = {
  active: ['paused', 'completed'] as const,
  paused: ['active', 'completed'] as const,
  completed: [] as const,
}

// ============================================================================
// Validation Schemas
// ============================================================================

const SessionStatusEnum = z.enum(['active', 'paused', 'completed'])

/**
 * POST /sessions — create or resume a session.
 * client_session_id acts as an idempotency key: if a session with this ID
 * already exists for the user, we return it instead of inserting a duplicate.
 */
const CreateSessionSchema = z.object({
  platform_slug: z.string().min(1).max(50),
  game_id: z.string().max(100).optional(),
  started_at: z.string().datetime(),
  sc_balance_start: z.number().min(0).optional(),
  gc_balance_start: z.number().min(0).optional(),
  /** Stable UUID generated by the extension — used for idempotent creation. */
  client_session_id: z.string().uuid().optional(),
})

/**
 * A single spin/transaction record. client_tx_id is required so the extension
 * can safely retry failed batches without creating duplicates.
 */
const TransactionSchema = z.object({
  /** Extension-generated stable UUID for this spin — idempotency key. */
  client_tx_id: z.string().uuid(),
  game_id: z.string().max(100).optional(),
  bet_amount: z.number().positive().max(999_999),
  win_amount: z.number().min(0).max(999_999),
  result: z.enum(['win', 'loss', 'bonus']),
  timestamp: z.string().datetime(),
  bonus_triggered: z.boolean().default(false),
  jackpot_hit: z.boolean().default(false),
  /** SC balance snapshot immediately after this spin. */
  balance_after: z.number().min(0).optional(),
  multiplier: z.number().positive().optional(),
  free_spin: z.boolean().default(false),
})

const BatchTransactionsSchema = z.object({
  session_id: z.string().uuid(),
  transactions: z
    .array(TransactionSchema)
    .min(1, 'At least one transaction required')
    .max(BATCH_TX_MAX, `Maximum ${BATCH_TX_MAX} transactions per batch`),
})

const UpdateBalanceSchema = z.object({
  sc_balance: z.number().min(0),
  gc_balance: z.number().min(0).optional(),
})

const EndSessionSchema = z.object({
  ended_at: z.string().datetime(),
  sc_balance_close: z.number().min(0).optional(),
  gc_balance_close: z.number().min(0).optional(),
  notes: z.string().max(500).optional(),
})

const ListSessionsQuerySchema = z.object({
  platform_id: z.string().uuid().optional(),
  status: SessionStatusEnum.optional(),
  start_date: z.string().date().optional(),
  end_date: z.string().date().optional(),
  page: z.coerce.number().int().min(1).default(1),
  page_size: z.coerce.number().int().min(1).max(100).default(20),
  sort_by: z
    .enum(['started_at', 'rtp', 'net_result', 'total_wagered', 'spin_count'])
    .default('started_at'),
  sort_dir: z.enum(['asc', 'desc']).default('desc'),
})

